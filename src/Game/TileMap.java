    public static void loadTiles(ArrayList<Drawable> drawables, ArrayList<Collidable> collidables) {
        int mapVertLimit = map.length - 1;
        int mapLatLimit = map[0].length - 1;

        for (int i = 0; i <= mapVertLimit; i++) {
            for (int j = 0; j <= mapLatLimit; j++) {

                int wallInt = map[i][j];

                if (wallInt != 0) {

                    BufferedImage img = null;
                    int x = j * GameWorld.TILE_WIDTH;
                    int y = i * GameWorld.TILE_HEIGHT;

                    switch (map[i][j]) {
                        case 1: try { img = read( TileMap.class.getClassLoader().getResourceAsStream("Wall1.gif")); } catch(Exception e){};
                                SolidWall wallS = new SolidWall(img, x, y);
                                drawables.add(wallS);
                                if( i != 0 && i != mapVertLimit && j != 0 && j != mapLatLimit)
                                    collidables.add(wallS);
                                break;
                        case 2: try { img = read(TileMap.class.getClassLoader().getResourceAsStream("Wall2.gif")); } catch(Exception e){};
                                BreakableWall wallB = new BreakableWall(img, x, y);
                                drawables.add(wallB);
                                collidables.add(wallB);
                                break;
                        case 3: try { img = read(TileMap.class.getClassLoader().getResourceAsStream("missilePowerup.png")); } catch(Exception e){};
                                MissilePowerUp missilePowerUp = new MissilePowerUp(img, x, y);
                                drawables.add(missilePowerUp);
                                collidables.add(missilePowerUp);
                                break;
                        case 4: try { img = read(TileMap.class.getClassLoader().getResourceAsStream("scramblerPowerup.png")); } catch(Exception e){};
                                ScramblerPowerUp scramblerPowerUp = new ScramblerPowerUp(img, x, y);
                                drawables.add(scramblerPowerUp);
                                collidables.add(scramblerPowerUp);
                                break;

                    }
                }
            }
        }
    }


